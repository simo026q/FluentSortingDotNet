namespace FluentSortingDotNet;

/// <summary>
/// Represents the direction of a sort operation.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Indicates that the sort operation should be performed in ascending order (e.g. 1, 2, 3).
    /// </summary>
    Ascending,

    /// <summary>
    /// Indicates that the sort operation should be performed in descending order (e.g. 3, 2, 1).
    /// </summary>
    Descending
}
